#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    ZTableViewController *tableViewController = [[ZTableViewController alloc]init];
    tableViewController.index = indexPath.row;
    
    ZNavigationViewController *navigationController = (ZNavigationViewController *)(self.parentViewController);
    [navigationController pushViewController:tableViewController andTabViewController:nil animated:YES];
}

